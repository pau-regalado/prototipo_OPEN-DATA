from geopy.geocoders import Nominatim
import folium
import pandas as pd
import json
import matplotlib.pyplot as plt
import seaborn as sns

tur = pd.read_excel("turismo-Can-Tnf-Lzn.xlsx")
ben = pd.read_excel("beneficioTurismo.xlsx")

# Contraste entre canarias, tenerife, lanzarote en numeros de turista mensuales

tur.plot(kind="line", x='Mes', y=['Lanzarote', 'Tenerife'])
